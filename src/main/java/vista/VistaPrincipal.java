    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {

    if (jTextField1.getText().trim().isEmpty() ||
        jTextField2.getText().trim().isEmpty() ||
        jTextField3.getText().trim().isEmpty() ||
        jTextField4.getText().trim().isEmpty() ||
        jTextField5.getText().trim().isEmpty() ||
        jTextField6.getText().trim().isEmpty()) {

        javax.swing.JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios");
        return;
    }

    // VALIDAR NOMBRE
    if (!jTextField1.getText().trim().matches("[a-zA-ZáéíóúÁÉÍÓÚñÑ ]+")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Nombre solo letras");
        return;
    }

    // VALIDAR TELEFONO
    if (!jTextField5.getText().trim().matches("\\d{10}")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Telefono debe tener 10 digitos");
        return;
    }

    // VALIDAR CODIGO
    if (!jTextField2.getText().trim().matches("\\d+")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Codigo invalido");
        return;
    }

    // VALIDAR GRADO (INT)
    int grado;
    try {
        grado = Integer.parseInt(jTextField4.getText().trim());
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Grado invalido");
        return;
    }

    // CREAR ESTUDIANTE
    Estudiante e = new Estudiante(
        jTextField1.getText().trim(),
        jTextField3.getText().trim(),
        jTextField5.getText().trim(),
        jTextField6.getText().trim(),
        jTextField2.getText().trim(),
        grado
    );

    controlador.agregarEstudiante(e);

    javax.swing.JOptionPane.showMessageDialog(this, "Estudiante agregado");

    // LIMPIAR CAMPOS
    jTextField1.setText("");
    jTextField2.setText("");
    jTextField3.setText("");
    jTextField4.setText("");
    jTextField5.setText("");
    jTextField6.setText("");

} catch (Exception ex) {
    ex.printStackTrace();
    javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage());
}
    }//GEN-LAST:event_jButton1ActionPerformed

