    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    try {

    // VALIDAR CAMPOS VACIOS
    if (jTextField9.getText().trim().isEmpty() || 
            jTextField1.getText().trim().isEmpty() ||
            jTextField2.getText().trim().isEmpty() ||
            jTextField3.getText().trim().isEmpty() ||
            jTextField5.getText().trim().isEmpty() ||
            jTextField6.getText().trim().isEmpty() ||
            jTextField7.getText().trim().isEmpty() ||
            jTextField8.getText().trim().isEmpty() ||
            jTextField4.getText().trim().isEmpty()) 
    {

        javax.swing.JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios");
        return;
    }

    // VALIDAR AREA
    String area = jTextField8.getText().trim();
    if (area.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar el area del profesor");
        return;
    }

    // VALIDAR CEDULA
    if (!jTextField7.getText().trim().matches("\\d+")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Cedula invalida");
        return;
    }

    // VALIDAR TELEFONO
    if (!jTextField5.getText().trim().matches("\\d{10}")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Telefono debe tener 10 digitos");
        return;
    }

    // VALIDAR SALARIO
    double salario;
        
    try {
        salario = Double.parseDouble(jTextField4.getText().trim());
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "salario invalido");
        return;
    }
    
    //validar horas
    int horas;
    try {
        horas=Integer.parseInt(jTextField9.getText().trim());
    }catch(Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this,"Horas invalidas");
        return;
        
    }

    // CREAR PROFESOR
    Profesor p = new Profesor(
        jTextField1.getText().trim(),
        jTextField2.getText().trim(),    
        jTextField5.getText().trim(),
        jTextField6.getText().trim(),
        jTextField7.getText().trim(),
        area, 
        salario,
        horas
    );

    controlador.agregarProfesor(p);

    javax.swing.JOptionPane.showMessageDialog(this, "Profesor agregado");

    // LIMPIAR CAMPOS
    jTextField1.setText("");
    jTextField2.setText("");
    jTextField3.setText("");
    jTextField4.setText("");
    jTextField5.setText("");
    jTextField6.setText("");
    jTextField7.setText("");
    jTextField8.setText("");
    jTextField9.setText("");
} catch (Exception ex) {
    ex.printStackTrace();
    javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage());
}
    }//GEN-LAST:event_jButton2ActionPerformed

